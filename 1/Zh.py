# -----------------------------
# Sentence templates
# -----------------------------
templates = [
    "{subject} {time} 去 {place} {action}",
    "{subject} {action} {object} {time}",
    "{subject} {time} {action} {object}",
    "你 {time} {action} {object} 吗？",
    "{subject} {action} {object}，{comment}。"
]

# -----------------------------
# Sentence generator
# -----------------------------
def generate_sentence():
    template = random.choice(templates)
    subject = random.choice(["我","你","他","她","我们"])
    time = random.choice(["今天","明天","昨天","早上","晚上","下午"])
    place = random.choice(["北京大学","公园","饭馆儿","学校","商店","家"])
    action = random.choice(["去","吃","看","买","学习","走","打电话","坐车","玩"])
    obj = random.choice(["米饭","汉语","衣服","火车票","朋友","鸡肉","电影"])
    comment = random.choice(["真的","最好","不一定","可能"])
    
    return template.format(subject=subject, time=time, place=place,
                           action=action, object=obj, comment=comment)

# -----------------------------
# Filter words from a text
# -----------------------------
def filter_words(text):
    return [w for w in chinese_words if w in text]

# -----------------------------
# Check if a word exists
# -----------------------------
def word_exists(word):
    return word in chinese_words

